const GITIGNORE_CONTENT = `# Theme Doctor generated files
.theme-doctor/
goldens/
audit/
reports/
`;

export async function initWorkspace(dir: string, opts: { force: boolean }): Promise<void> {
  const configPath = path.join(dir, 'theme-doctor.yaml');

  // Bug fix: use atomic write so a crash mid-init doesn't corrupt the config
  if (!fs.existsSync(configPath) || opts.force) {
    await atomicWriteFile(configPath, DEFAULT_CONFIG);
  }

  // Write .gitignore addition
  const gitignorePath = path.join(dir, '.theme-doctor-gitignore');
  if (!fs.existsSync(gitignorePath) || opts.force) {
    await atomicWriteFile(gitignorePath, GITIGNORE_CONTENT);
  }

  // Copy default rubric files
  const defaultsExist = fs.existsSync(DEFAULTS_DIR);

  if (defaultsExist) {
    await copyDefault(
      path.join(DEFAULTS_DIR, 'rubric', 'templates.yaml'),
      path.join(dir, 'rubric', 'templates.yaml'),
      opts.force,
    );
    await copyDefault(
      path.join(DEFAULTS_DIR, 'rubric', 'flows.yaml'),
      path.join(dir, 'rubric', 'flows.yaml'),
      opts.force,
    );
    await copyDefault(
      path.join(DEFAULTS_DIR, 'blueprints', 'base.json'),
      path.join(dir, 'blueprints', 'base.json'),
      opts.force,
    );
  } else {
    // Fallback: write inline defaults
    await writeFallbackDefaults(dir, opts.force);
  }

  // Create placeholder dirs
  for (const sub of ['goldens', 'audit', 'reports', '.theme-doctor']) {
    await fsp.mkdir(path.join(dir, sub), { recursive: true });
    const keep = path.join(dir, sub, '.gitkeep');
    if (!fs.existsSync(keep)) await fsp.writeFile(keep, '');
  }
}

async function writeFallbackDefaults(dir: string, force: boolean): Promise<void> {
  const rubricDir   = path.join(dir, 'rubric');
  const blueprintDir = path.join(dir, 'blueprints');
  await fsp.mkdir(rubricDir, { recursive: true });
  await fsp.mkdir(blueprintDir, { recursive: true });

  const tplPath = path.join(rubricDir, 'templates.yaml');
  if (!fs.existsSync(tplPath) || force) {
    await atomicWriteFile(tplPath, FALLBACK_TEMPLATES_YAML);
  }

  const flowPath = path.join(rubricDir, 'flows.yaml');
  if (!fs.existsSync(flowPath) || force) {
    await atomicWriteFile(flowPath, FALLBACK_FLOWS_YAML);
  }

  const bpPath = path.join(blueprintDir, 'base.json');
  if (!fs.existsSync(bpPath) || force) {
    await atomicWriteFile(bpPath, FALLBACK_BLUEPRINT_JSON);
  }
}

