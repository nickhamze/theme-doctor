async function copyDefault(src: string, dest: string, force: boolean): Promise<boolean> {
  if (fs.existsSync(dest) && !force) return false;
  await fsp.mkdir(path.dirname(dest), { recursive: true });
  await fsp.copyFile(src, dest);
  return true;
}

